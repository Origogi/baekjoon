package algo;


import java.util.*;

//2741. N 찍기
//https://www.acmicpc.net/problem/2741

public class M2741 {

    public static void main(String[] args) {

        Scanner scan = new Scanner(System.in);
        int n = scan.nextInt();
        scan.close();
        for (int i = 1;i<=n;i++)
            System.out.println(i);
    }
}
